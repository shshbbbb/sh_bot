def send_to_telegram(message):

    url = f"https://api.telegram.org/bot{BOT_TOKEN}/sendMessage"

    for chat_id in [CHANNEL_ID, CHANNEL_ID_2]:

        payload = {
            "chat_id": chat_id,
            "text": message
        }

        result = requests.post(
            url,
            json=payload
        )

        print(result.json())


